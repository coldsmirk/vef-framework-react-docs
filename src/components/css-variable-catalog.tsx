export const variables: CssVariableEntry[] = rawVariableText
  .split("\n")
  .map(line => line.trim())
  .filter(Boolean)
  .map(line => line.replace(/,$/, ""))
  .map((line) => {
    const match = line.match(/^(--[^:]+):\s*(.+);$/);

    if (!match) {
      throw new Error(`Failed to parse CSS variable line: ${line}`);
    }

    return {
      name: match[1],
      value: match[2]
    };
  });

