export function getColorFamily(name: string) {
  const match = name.match(/^--vef-color-([a-z]+)-(\d+)$/);
  return match?.[1] || null;
}

export function getPresetScaleFamily(name: string) {
  const match = name.match(/^--vef-([a-z]+)-(\d+)$/);
  return match?.[1] || null;
}

export function classifyVariable(name: string) {
  if (
    name === "--vef-color-inverted" ||
    name.startsWith("--vef-spacing-") ||
    name.startsWith("--vef-shadow-") ||
    name.startsWith("--vef-breakpoint-") ||
    name.startsWith("--vef-animate-")
  ) {
    return "foundation";
  }

  const semanticScaleFamily = getColorFamily(name);

  if (semanticScaleFamily && semanticScaleFamilies.has(semanticScaleFamily)) {
    return "semantic-scales";
  }

  if (semanticScaleFamily && extendedPaletteFamilies.has(semanticScaleFamily)) {
    return "extended-palettes";
  }

  const presetScaleFamily = getPresetScaleFamily(name);

  if (presetScaleFamily && presetAliasFamilies.has(presetScaleFamily)) {
    return "preset-scales";
  }

  if (
    presetAliasFamilies.has(name.replace("--vef-", "")) ||
    [
      "--vef-color-primary",
      "--vef-color-success",
      "--vef-color-warning",
      "--vef-color-error",
      "--vef-color-info",
      "--vef-color-link",
      "--vef-color-text-base",
      "--vef-color-bg-base"
    ].includes(name)
  ) {
    return "base-aliases";
  }

  if (
    /^--vef-color-(primary|success|warning|error|info|link)-(bg|bg-hover|bg-filled-hover|bg-active|border|border-hover|hover|active|text-hover|text|text-active)$/.test(
      name
    )
  ) {
    return "semantic-states";
  }

  if (
    /^--vef-(blue|purple|cyan|green|magenta|pink|red|orange|yellow|volcano|geekblue|gold|lime)-(hover|active)$/.test(
      name
    )
  ) {
    return "semantic-states";
  }

  if (
    name.startsWith("--vef-font-family") ||
    name.startsWith("--vef-font-size") ||
    name.startsWith("--vef-line-height") ||
    name.startsWith("--vef-font-height") ||
    name === "--vef-font-weight-strong"
  ) {
    return "typography";
  }

  if (name.startsWith("--vef-box-shadow")) {
    return "box-shadows";
  }

  if (
    name.startsWith("--vef-motion-") ||
    name.startsWith("--vef-z-index-") ||
    name.startsWith("--vef-opacity-") ||
    name.startsWith("--vef-link-")
  ) {
    return "motion-and-stack";
  }

  if (
    name.startsWith("--vef-border-radius") ||
    name.startsWith("--vef-size-") ||
    name.startsWith("--vef-control-height") ||
    name.startsWith("--vef-padding-") ||
    name.startsWith("--vef-margin-") ||
    name.startsWith("--vef-control-padding-horizontal") ||
    name === "--vef-size" ||
    name === "--vef-padding" ||
    name === "--vef-margin" ||
    name === "--vef-line-width" ||
    name === "--vef-line-type" ||
    name === "--vef-line-width-bold" ||
    name === "--vef-line-width-focus" ||
    name === "--vef-control-outline-width" ||
    name === "--vef-control-interactive-size"
  ) {
    return "layout";
  }

  if (
    name.startsWith("--vef-color-") ||
    name.startsWith("--vef-control-") ||
    name === "--vef-color-white"
  ) {
    return "surface-and-control";
  }

  return "surface-and-control";
}

